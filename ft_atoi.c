#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	int		nbr;
	int		n;

	i = 0;
	nbr = 0;
	n = 0;
	while (str[i] != '\0' && (str[i] == 32 || (str[i] >= 9 && str[i] <= 13)))
		i++;
	if (str[i] != '\0' && str[i] == '-')
	{
		n = 1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] != '\0' && ft_isdigit(str[i]))
		nbr = (nbr * 10) + (str[i++] - '0');
	if (n == 1)
		return (-nbr);
	return (nbr);
}
